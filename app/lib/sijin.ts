// 저장된 시각(HH:MM)이 어느 시진의 중간값과 정확히 일치하면 그 시진을 돌려준다(편집 복귀 역매핑).
// 일치하는 게 없으면 null → "정확한 시각"으로 저장된 값이라는 뜻이다.
export function sijinOfTime(time: string | undefined | null): Sijin | null {
  if (!time) return null;
  return SIJIN.find((s) => s.mid === time) ?? null;
}
