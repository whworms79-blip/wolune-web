// 12시진(時辰) — 태어난 시간을 시각(분) 대신 시진으로 고를 수 있게 하는 데이터.
//
// 왜 시진인가: 대부분의 사람은 자기 태어난 시각을 분 단위로 모른다("저녁에 태어났다"만 안다).
// 정밀 시각 입력을 기본으로 두면 그들을 배제한다. 그래서 시진 선택을 기본으로.
//
// ★ mid(중간값 HH:MM)를 엔진에 넘긴다 — 시진 구간은 120분이라 중간값은 경계에서 60분 여유가
//   있고, 서울 최대 진태양시 보정(-48분: 경도 -32 + 균시차 -16)보다 크다. 그래서 12시진 중간값이
//   보정 후에도 **모두 같은 시진에 남는다**(실측 확인: 균시차 극단 3날짜 × 12시진 = 어긋남 0).
//   즉 진태양시 정밀 계산이 그대로 유지된다.
//
// ⚠ label 에 분 단위 구간(19:30~21:30)을 **절대 넣지 않는다.** 사용자가 자기 시계 시각과
//   대조하다 함정에 빠진다(예: 23:40 을 자시로 착각 — 실제론 진태양시 보정으로 해시).
//   일상어 시간대("저녁 7~9시")로만 표현해 그 대조 자체를 막는다.
//   정밀한 시각을 아는 사람은 "정확한 시각" 옵션으로 분까지 넣는다.

export interface Sijin {
  key: string; // "sul"
  name: string; // "술시"
  daily: string; // "저녁 7~9시"  (분 단위 구간 금지)
  mid: string; // "20:30" — 엔진에 넘길 중간값 시각
}

